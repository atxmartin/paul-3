    public void initMonitor() {
        String[] columnNames = {"Kundennummer",
                        "Hausnummer",
                        "Wohnungsnummer",
                        "Art",
                        "Z채hlernummer",
                        "Ablesedatum",
                        "Gewechselt",
                        "Kraftstrom in kWh",
                        "Haushaltsstrom in kWh",
                        "Kommentar"};
        
        MessdatenListe messDaten = io.getMessdatenListe();
        Object[][] data = new Object[messDaten.getMessdatenListe().size()][10];
        
        for(int i = 0; i < io.getMessdatenListe().getMessdatenListe().size(); i++)
        {
            MessdatenEintrag entry = messDaten.getMessdatenListe().get(i);
            data[i][0] = entry.getKundennr();
            data[i][1] = entry.getHausnr();
            data[i][2] = entry.getWohnungsnr();
            data[i][3] = entry.getZaehlerart();
            data[i][4] = entry.getZaehlernr();
            
            SimpleDateFormat originalFormat = new SimpleDateFormat("yyyyMMdd");
            Date date = null;
            try {
                date = originalFormat.parse(String.valueOf(entry.getAblesedatum()));
            } catch (ParseException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
            SimpleDateFormat newFormat = new SimpleDateFormat("dd.MM.yyyy");
            String formatedDate = newFormat.format(date);
            data[i][5] = formatedDate;
            
            if(entry.isZahlerwechsel()){
                data[i][6] = "Ja";
            }else{
                data[i][6] = "";
            }
            data[i][7] = entry.getZaehlerstandK();
            data[i][8] = entry.getZaehlerstandH();
            data[i][9] = entry.getKommentar();
        }
        
        JTable table = new JTable(data, columnNames);
        table.setDefaultEditor(Object.class, null);
        table.setShowVerticalLines(false);
        table.setRowHeight(20);
        
        monitoringScrollPane.setViewportView(table);
        table.setFillsViewportHeight(true);
    }
